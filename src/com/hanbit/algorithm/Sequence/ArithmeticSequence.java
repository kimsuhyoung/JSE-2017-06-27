package com.hanbit.algorithm.Sequence;
public class ArithmeticSequence{
	public static void main(String[] args){
	
		for(int i=1;i<10;i++){
			for(int j=2;j<6;j++){
				System.out.print(i+"*"+j+"="+j*i+"\t");			
			}
				System.out.print("\n");
		}System.out.print("\n"); 
		
		for(int o=1;o<10;o++){ 
			for(int k=6;k<10;k++){ 
				System.out.print(k+"*"+o+"="+o*k+"\t");
			}
				System.out.print("\n");
		}
	}
}